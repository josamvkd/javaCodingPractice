package com.repeat;

public class TypeCastingDemo
{
    static void main()
    {
        double d = 5.5;
        System.out.println("**Before typecasting**");
        System.out.println(d);
        int i = (int) d;
        System.out.println("**After typecasting**");
        System.out.println(i);
    }
}
